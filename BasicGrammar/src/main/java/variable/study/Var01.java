public class Var01 {
    /**
     * 变量的使用
     *      1:声明时赋值
     *          数据类型  变量名 = 值;
     *      2:先声明后赋值
     *          数据类型   变量名;
     *          变量名 = 值;
     */
    public static void main(String[] args) {
        //声明时赋值
        int a = 10;
        //先声明后赋值
        int b;
        b  = 20;
        System.out.println(a);
        System.out.println(b);
    }
}
